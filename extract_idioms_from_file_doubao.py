def extract_idioms_from_file(file_path):
    idiom_list = []
    try:
        # 打开文件
        with open(file_path, 'r', encoding='utf-8') as file:
            # 逐行读取文件内容
            for line in file:
                # 检查行中是否包含空格
                if ' ' in line:
                    # 使用空格分割行内容
                    idioms = line.strip().split(' ')
                    # 将分割后的成语添加到成语列表中
                    idiom_list.extend(idioms)
    except FileNotFoundError:
        print(f"文件 {file_path} 未找到。")
    except Exception as e:
        print(f"读取文件时发生错误: {e}")
    return idiom_list

def find_duplicate_idioms(idiom_list):
    # 创建一个空字典，用于记录每个成语的出现次数
    idiom_count = {}

    # 遍历成语列表，统计每个成语的出现次数
    for idiom in idiom_list:
        if idiom in idiom_count:
            idiom_count[idiom] += 1
        else:
            idiom_count[idiom] = 1

    # 找出重复的成语
    duplicate_idioms = [idiom for idiom, count in idiom_count.items() if count > 1]

    return duplicate_idioms

